public class VariableIntroduction {

    public static void main(String[] args) {

        //numerical types: int and double
        //int: whole number, + or - or 0.
        //double: floating point number, decimal. about 15 sig figs of accuracy.

        //declare a variable. type and name

        int count;
        count = 0;
        System.out.println(count);
        int two = 2;
        System.out.println(two);
        double x = 4;
        System.out.println(x);

        //boolean type hold on to true or false data

        boolean win = false;
        System.out.println(win);
        win = true;
        System.out.println(win);
        String ya = "a ";
        System.out.println(ya);
    }
}
